    func showLanguagePicker(from vc: UIViewController, localeDidChanged: (()->())? = nil) {
        let options = GlobalConstants.sheetOptions
        let languagePickerVC = LanguagePickerViewController(selectedLocale: DictionaryManager.shared.locale)

        let sheetController = SheetViewController(
            controller: languagePickerVC,
            sizes: [.intrinsic],
            options: options
        )
        sheetController.contentBackgroundColor = .clear
        sheetController.cornerRadius = 30
        sheetController.cornerCurve = .continuous
        sheetController.shouldRecognizePanGestureWithUIControls = false
        sheetController.allowPullingPastMaxHeight = false
        vc.present(sheetController, animated: true, completion: nil)

        languagePickerVC.onLocaleSelected = { locale in
            guard let locale = locale else { return }
            DictionaryManager.shared.locale = locale
            localeDidChanged?()
        }
    }
